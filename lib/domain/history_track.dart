class HistoryTrack {
  final int played;
  final String title;
  final String artist;
  final String albumID;
  final String album;
  final String albumArt;
  final String circle;
  final int track;

  const HistoryTrack({
    required this.played, 
    required this.title, 
    required this.artist, 
    required this.albumID, 
    required this.album, 
    required this.albumArt, 
    required this.circle, 
    required this.track
  });
}